#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;

	if (!s)
		return (NULL);
	str = (unsigned char *)s;
	while (0 < n--)
		if (*(str++) == (unsigned char)c)
			return (--str);
	return (0);
}
